  public void displayTable(){
    ArrayList<LoanApproval> la = conn.displayLoans();
    tbl.setRowCount(0);
    for (LoanApproval loan : la) {
        
        String loanStatus = loan.getLoanStatus() == 2 ? "PENDING" : String.valueOf(loan.getLoanStatus());
        String loanType = loan.getLoanType();
        String data[] = {
            String.valueOf(loan.getLoanID()),
            String.valueOf(loan.getUserID()),
            loanType,
            String.valueOf(loan.getLoanAmount()),
            String.valueOf(loan.getInterestRate()),
            String.valueOf(loan.getLoanTerm()),
            loanStatus
        };
        tbl.addRow(data);
    }
}



