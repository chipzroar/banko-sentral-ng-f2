    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
    int index = tblDash.getSelectedRow();
    String loanIDString = (String) tblDash.getValueAt(index, 0);
    int loanID = Integer.parseInt(loanIDString);

    if(conn.changeLoanStatus(loanID, 1)){
        JOptionPane.showMessageDialog(null, "Loan has been approved");
        txtStatus.setText("");
    } else {
        JOptionPane.showMessageDialog(null, "Error while approving loan");
    }

    displayTable();
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnDeclineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeclineActionPerformed
        // TODO add your handling code here:
    int index = tblDash.getSelectedRow();
    String loanIDString = (String) tblDash.getValueAt(index, 0);
    int loanID = Integer.parseInt(loanIDString);

    if(conn.changeLoanStatus(loanID, 0)){
        JOptionPane.showMessageDialog(null, "Loan has been declined");
        txtStatus.setText("");
    } else {
        JOptionPane.showMessageDialog(null, "Error while declining loan");
    }

    displayTable();
    }//GEN-LAST:event_btnDeclineActionPerformed

